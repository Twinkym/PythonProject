# Ejemplo de operadores aritmeticos.
# a = 30
# b = 3
a, b = 10, 3

# suma +
suma = a + b
print(f'suma: {suma}')

# Resta -
resta = a - b
print(f'Resta: {resta}')

# Multiplicación *
multiplicacion = a * b
print(f'Multiplicación: {multiplicacion}')

# División /
division = a / b
print(f'División: {division}')

# División entera //
division_Exacta = a // b
print(f'Division Exacta: {division_Exacta}')

# Resto (resto de la division) %
resto = a % b
print(f'Resto: {resto}')

# Potencia **
potencia = a ** b  # 10 elevado a la 3 -> 10*10*10
print(f'Potencia: {potencia}')
