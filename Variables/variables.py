# Programa para definir variables en Python.
nombre = 'David'
edad = 49
pais = 'España'

# Imprimimos o mostramos en consola los valores de cada variable.
print('Nombre: ', nombre)
print('Edad: ', edad)
print('Pais: ', pais)

# Modificar los valores de las variables
nombre = 'David De La Puente'
edad = 50
print('Nombre: ', nombre)
print('Edad: ', edad)
print('Pais: ', pais)

# En Python el tipo es dinámico
edad = 'Treinta'
print('Edad: ', edad)

