# Caracteres especiales
print("Hola\nMundo")  # \n este caracter agrega un salto de linea
print("Python es \tgenial")  # \t este caracter agrega una tabulación
print(
    "Juan\" Perez")  # Agregar un slash invertido previo a comillas simples o dobles esto pintará la comilla en la pantalla
print('Karla\' Gomez')
